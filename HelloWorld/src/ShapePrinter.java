public class ShapePrinter {

    public static void main(String[] args) {
        
        // Imprimir uma caixa
        System.out.println("*********");
        System.out.println("*       *");
        System.out.println("*       *");
        System.out.println("*********");

        System.out.println();

        // Imprimir uma elipse (aproximadamente)
        System.out.println("  ***  ");
        System.out.println("*******");
        System.out.println("*******");
        System.out.println("  ***  ");

        System.out.println();

        // Imprimir uma seta
        System.out.println("   *   ");
        System.out.println("  ***  ");
        System.out.println(" ***** ");
        System.out.println("*******");
        System.out.println("   *   ");
        System.out.println("   *   ");

        System.out.println();

        // Imprimir um losango (aproximadamente)
        System.out.println("   *   ");
        System.out.println("  ***  ");
        System.out.println(" ***** ");
        System.out.println("*******");
        System.out.println(" ***** ");
        System.out.println("  ***  ");
        System.out.println("   *   ");
    }
}
